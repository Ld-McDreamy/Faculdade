# OPERADORES LÓGICOS
# 
# not - Negação
# and - Conjunção
# or - Disjunção

Aprov1 = True
Aprov2 = False

print(Aprov1 and Aprov2) # Aquela tabela verdade E // Verdadeiro ^ Falso = False
print(Aprov1 or Aprov2) # Aquela tabela verdade OU // Verdadeiro v Falso = True
print(not Aprov1) # Aquela tabela verdade NÃO É VERDADE QUE  // ~Verdadeiro = False
